    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // TODO add your handling code here:

        boolean incorrecto = false;

        String nombre = txtNombre.getText();
        String login = txtLogin.getText();
        int identificador = 0;
        
        //comprobacion para que salte error si el identificador no es numerico
        try {
            identificador = Integer.parseInt(txtIdentificador.getText());//se convierte lo que halla en el campo de identificador a numero
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El identificador debe ser numerico", "Error", JOptionPane.ERROR_MESSAGE);
            incorrecto = true;
            txtIdentificador.setText("");
        }
        
        String contra = new String(pwdContra.getPassword());//getPassword--> para metodo que da la contrase単a (devuelve un char por lo que hay que convertirlo a String)
        String repite = new String(pwdContra1.getPassword());
        int tipo = cmbTipoUsuario.getSelectedIndex();//da el indice del elemento seleccionado (<0> para usuario y <1> si es administrador)

        //comprobacion de que los datos estan bien 
        if (!(contra.equals(repite))) {
            JOptionPane.showMessageDialog(this, "La contrase単a no coincide", "Error", JOptionPane.ERROR_MESSAGE);
            incorrecto = true;
            pwdContra1.setText("");
            pwdContra.setText("");
        }
        if (email.buscarEmpleadoLogin(login) != -1) {
            JOptionPane.showMessageDialog(this, "Ya existe un usuario con ese login", "Error", JOptionPane.ERROR_MESSAGE);
            incorrecto = true;
            txtLogin.setText("");
        }

        if (email.buscarEmpleadoIdentificador(identificador) != -1) {
            JOptionPane.showMessageDialog(this, "Ya existe un usuario con ese identificador", "Error", JOptionPane.ERROR_MESSAGE);
            incorrecto = true;
            txtIdentificador.setText("");
        }

        //si los datos estan correctos, se crea el empleado
        if (!incorrecto) {
            Empleado e = new Empleado(identificador, login, contra, nombre, tipo);
            email.anadirEmpleado(e);
        }


    }//GEN-LAST:event_btnGrabarActionPerformed


