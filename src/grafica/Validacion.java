    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String login = txtLogin.getText();
        String contra = txtContra.getText();

        int pos = email.buscarEmpleado(login, contra);

        if (pos == -1) {
            JOptionPane.showMessageDialog(this, "Este usuario no existe");
            txtLogin.setText("");
            txtContra.setText("");
            txtLogin.requestFocus();
        } else {
            //si el usuario es correcto, pasa a la ventana del usuario
            int identificador = email.getMisEmpleados().get(pos).getIdentificador();
            new Administrador(email, identificador).setVisible(true);
            dispose();
            pack();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

